package exemplos;

public class Exemplo01{
    public static void main(String[] args) {
        int cont;

        cont = 1; //INICIALIZAÇÃO
        while (cont < 10) {  //CONDIÇÃO: enquanto for verdadeiro
            System.out.println(cont);
            cont++; //INCREMENTO: passo
            
        }

        System.out.println("\nFim");
    }
}